CREATE TABLE  F (
	c  INTEGER,
	d INTEGER,
	PRIMARY KEY(c)
);

CREATE TABLE  H (
	g INTEGER,
	h INTEGER,
	PRIMARY KEY (g)
);

CREATE TABLE  E (
	c INTEGER,
	a INTEGER,
	b INTEGER,
	PRIMARY KEY (a,c),
	FOREIGN KEY (c) REFERENCES F(c)
);

CREATE TABLE G  (
	c INTEGER,
	a INTEGER,
	e INTEGER,
	PRIMARY KEY (a,c),
	FOREIGN KEY (a,c) REFERENCES E(a,c)
);

CREATE TABLE S  (
	c INTEGER,
	a INTEGER,
	g INTEGER,
	PRIMARY KEY (a,c,g),
	FOREIGN KEY (a,c) REFERENCES G(a,c),
	FOREIGN KEY (g) REFERENCES H(g)
);